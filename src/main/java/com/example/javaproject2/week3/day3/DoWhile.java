package com.example.javaproject2.week3.day3;

public class DoWhile {
    public static void main(String[] args) {
        int num = 10;

        System.out.println("== while 시작 ==");

        do {
            System.out.println("실행");
        }while (num < 10);

        System.out.println("== while 종료 ==");
    }
}
