package com.example.javaproject2.week2.day3;

public class LikeAccumulate {
    public static void main(String[] args) {
        int likeCount = 0;
        likeCount = likeCount + 1;
        System.out.println("좋아요 수 = " + likeCount);

        likeCount = likeCount + 1;
        System.out.println("좋아요 수 = " + likeCount);

        likeCount = likeCount - 1;
        System.out.println("좋아요 수 = " + likeCount);

    }
}
