package com.example.javaproject2.week2.day3;

public class ArithmeticOperators1 {

    public static void main(String[] args) {

        int a = 10;
        int b = 3;

        System.out.printf("a + b = %d\n", 3 + 10);
        System.out.printf("a - b = %d\n", 3 - 10);
        System.out.printf("a * b = %d\n", 3 * 10);
        System.out.printf("a / b = %d\n", 3 / 10);
        System.out.printf("a %% b = %d\n", 3 % 10); // printf 쓸떄 % 연산자는 %%로 써주기
    }
}
